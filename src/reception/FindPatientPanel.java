    private void mousePressedActionPerformed(MouseEvent e) {
        Point p = e.getPoint();
        int row = jTable1.rowAtPoint(p);
        if (patients != null) {
            this.modifyPanel.setPatient(patients.get(row));
            setVisible(false);
            this.modifyPanel.setVisible(true);
            System.out.println("Get " + row + " patient");
        }

    }

