    private void displayPatients(ArrayList<Patient> patients) {

        for (Patient p : patients) {

            int id = p.getId();
            String firstName = p.getName();
            String surname = p.getSurname();
            Date dob = p.getDob();
            String postcode = p.getAddress().getPostcode();
            String phone = p.getPhoneNumber();
            String healthCarePlan = p.getPlanName();
            Object[] row = { id, firstName, surname, dob, postcode, phone, healthCarePlan };
            model.addRow(row);

        }
        repaint();

    }

    private String getFirstName() {
        return nameTextField.getText();
    }

    private String getSurname() {
        return surnameTextField.getText();
    }
    
    public void cleanTable() {
        for (int i = 0; i < model.getRowCount(); i++) {
            for (int j = 0; j < model.getColumnCount(); j++) {
                
                model.setValueAt("", i, j);
            }
        }
    }

